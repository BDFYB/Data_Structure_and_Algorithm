void quick_sort(vector<int> &inputs, int low, int high) {
    if (low >= high) {
        return;
    }
    int i = low;
    int j = high;
    int flag_value = inputs[low];
    while (i < j) {
        while (i < j && inputs[j] >= flag_value) {
            j--;
        }
        inputs[i] = inputs[j];
        while (i < j && inputs[i] < flag_value) {
            i++;
        }
        inputs[j] = inputs[i];
    }
    inputs[i] = flag_value;
    quick_sort(inputs, low, i);
    quick_sort(inputs, i + 1, high);
};


